var FHIRServer = require('@asymmetrik/node-fhir-server-core')
var careconnect = FHIRServer

const coreFhirVersion = '3_0_1'

careconnect.ccresolveSchema = (baseVersion, resourceModule) => {
  // TODO We don't support version yet (and our file structure will probably change)
  // I think the world might end if we continue this path join nonsense to support versions. There has to be a better way? EJ.
  // See if we have the module in care-connect - otherwise fall back to the asymmetrik library
  let base = path.join(__dirname, 'Profiles') // We should add to the constants so we can specify more acurately where stuff is. i.e include the path
  try {
    fs.accessSync(path.join(base, resourceModule), fs.constants.R_OK)
    return path.join(base, resourceModule)
  } catch (err) {
    return FHIRServer.resolveSchema(baseVersion, resourceModule)
  }
}

careconnect.care_connect_initialise = (config) => {
  let utils = require('./utils.js')
  utils.monkeyPatchConfig(config)
  return FHIRServer.initialize(config)
}

